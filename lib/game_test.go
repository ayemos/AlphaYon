func TestJudge(t *testing.T) {
	for _, test := range boardsToTest {
		b := NewBoard(4)
		err := b.loadArray(test.pins)

		if err != nil {
			t.Errorf("Setup error: %s.", err)
		}

		status := Judge(b)

		if status != test.status {
			t.Errorf("Judge(%s) = %s, want %s.", b, status, test.status)
		}
	}
}
